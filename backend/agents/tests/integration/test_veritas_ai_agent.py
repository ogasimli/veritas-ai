import dotenv
from google.adk.agents.run_config import RunConfig, StreamingMode
from google.adk.runners import Runner
from google.adk.sessions import InMemorySessionService
from google.genai import types

from veritas_ai_agent.agent import root_agent

dotenv.load_dotenv()


def test_agent_stream() -> None:
    """
    Integration test for the agent stream functionality.
    Tests that the agent returns valid streaming responses.
    """

    session_service = InMemorySessionService()

    session = session_service.create_session_sync(user_id="test_user", app_name="test")
    runner = Runner(agent=root_agent, session_service=session_service, app_name="test")

    # Using a typical audit input message
    message = types.Content(
        role="user",
        parts=[types.Part.from_text(text="Audit this financial statement.")],
    )

    events = list(
        runner.run(
            new_message=message,
            user_id="test_user",
            session_id=session.id,
            run_config=RunConfig(streaming_mode=StreamingMode.SSE),
        )
    )
    assert len(events) > 0, "Expected at least one message"

    # We expect some events, even if just tool calls or thoughts.
    # Since veritas_ai_agent is complex, we might not get a final answer immediately without mock tools,
    # but the runner should not crash and should produce events.
